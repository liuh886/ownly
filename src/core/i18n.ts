export type WYQDTranslationKey = keyof typeof translations.en;

export function normalizeWYQDLanguage(value: unknown): WYQDLanguage {
  return value === 'zh' ? 'zh' : 'en';
}

export function createWYQDTranslator(language: unknown) {
  const normalizedLanguage = normalizeWYQDLanguage(language);
  const dictionary = translations[normalizedLanguage];

  return {
    language: normalizedLanguage,
    t(key: WYQDTranslationKey) {
      return dictionary[key] ?? translations.en[key] ?? key;
    },
  };
}
